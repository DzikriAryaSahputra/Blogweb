                    <br>
                    <br>
                    “Dia mengambil tongkat estafet dari era di mana orang-orang diberitahu bahwa membaca manga akan membuat mereka bodoh, dan dia adalah salah satu orang yang menciptakan era di mana orang dewasa dan anak-anak dapat menikmati membaca manga. Dia menunjukkan kepada kita bahwa manga dapat melakukan banyak hal, dan dapat membawa kita ke dunia nyata. Rasanya seperti menyaksikan seorang pahlawan menyerang ke depan. Bukan hanya bagi para seniman manga, keseruan dan emosi era serialisasi Dragon Ball harus berakar pada masa kecil para kreator di semua industri. Keberadaannya bagaikan pohon besar,” tutur Eiichiro Oda. Sementara, pencipta Naruto Masashi Kishimoto menulis dalam sebuah pernyataan, juga melalui Shonen Jump: “Saya tumbuh dengan manga Mr. Toriyama, Dr. Slump di sekolah dasar dan Dragon Ball di sekolah menengah. Wajar bagi saya untuk memiliki manga Pak Toriyama di samping saya sebagai bagian dari hidup saya. Bahkan saat aku sedang sedih, acara mingguan Dragon Ball selalu membuatku melupakannya. Itu adalah keselamatan bagi saya, seorang anak desa yang tidak melakukan apa-apa. Itulah betapa aku menikmati Dragon Ball!”
